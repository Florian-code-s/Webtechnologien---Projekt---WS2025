      <h1>Flexbox</h1>
      <p class="lead">Flexbox ist ein leistungsfähiges CSS‑Layoutmodell für einachsig ausgerichtete Layouts. Es vereinfacht Ausrichtung, Reihenfolge und Verteilung von Raum innerhalb von Containern.</p>

      <h2>Wichtige Eigenschaften</h2>
      <ul>
        <li><code>display: flex;</code> macht den Container zum Flex‑Container.</li>
        <li><code>flex-direction</code> bestimmt Hauptachse (row/column).</li>
        <li><code>justify-content</code> verteilt Elemente entlang der Hauptachse.</li>
        <li><code>align-items</code> richtet Elemente entlang der Querachse aus.</li>
      </ul>

      <h2>Beispiel</h2>
      <pre><code>.container { display: flex; justify-content: space-between; align-items: center; }
.item { flex: 1; }</code></pre>

