      <h1>Typography</h1>
      <p class="lead">Typografie beeinflusst Leserlichkeit und Erscheinungsbild. Wichtige Themen: Schriftfamilien, Größe, Zeilenhöhe und responsive Skalierung.</p>

      <h2>Wichtige Konzepte</h2>
      <ul>
        <li><strong>Font‑Stack:</strong> Definiere eine Fallback‑Reihenfolge: <code>font-family: 'Inter', Arial, sans-serif;</code></li>
        <li><strong>Relative Einheiten:</strong> Nutze <code>rem</code> oder <code>em</code> statt festen Pixelwerten für bessere Skalierung.</li>
        <li><strong>Zeilenhöhe:</strong> <code>line-height</code> verbessert Lesbarkeit — typischerweise zwischen 1.2 und 1.6.</li>
      </ul>

      <h2>Responsive Typografie</h2>
      <p>Variiere Schriftgrößen mit Media Queries oder nutze <code>clamp()</code> für flüssige Skalierung.</p>

